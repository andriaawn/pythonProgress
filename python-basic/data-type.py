# text type : str
# numeric type : int, float, complex
# sequence type : list, tuple, range. example : list = [1,2,3], tuple = (1,2,3), range(1,10)
# mapping type : dict. example : dict = {'name':'john', 'age':25}
# set type : set, frozenset. example : set = {1,2,3}, frozenset = (1,2,3)
# boolean type : bool. example : True, False
# binary type : bytes, bytearray, memoryview. example : bytes = b'hello', bytearray = bytearray(5), memoryview = memoryview(bytes(5))
# none type : None

