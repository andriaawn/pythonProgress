# variables can be named using letters, numbers, and underscores
# variables cannot start with a number
# variables are case sensitive
#
# # Legal variable names:  
# myvar = "John"
# my_var = "John"
# _my_var = "John"
# myVar = "John"
# MYVAR = "John"
# myvar2 = "John"

# # Illegal variable names:
# 2myvar = "John"
# my-var = "John"
# my var = "John"

# camelCase
# myVariableName = "John"

# PascalCase
# MyVariableName = "John"

# snake_case
# my_variable_name = "John"
# kebab-case
# my-variable-name = "John"



